def superDigit(n, k):
    # Write your code here
    vetor = list(map(int, n))
    if (k*len(n) == 1):
        return vetor[0]
    soma = 0
    for elemento in vetor:
        soma +=elemento
    soma = k*soma
    nova_string = str(soma)
    return superDigit(nova_string, 1)
        

